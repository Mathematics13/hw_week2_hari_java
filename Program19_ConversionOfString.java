package java_homework;

/** Program 19
 * Java Program to convert a given string
 * From Uppercase to lowercase
 */

public class Program19_ConversionOfString
{
    public static void main(String[] args)                             //Method declaration
    {
        String s1 = "THE QUICK BROWN FOX JUMPS OVER THE LAZY DOG";    //String declaration and initialization in UpperCase
        String s2= s1.toLowerCase();                                 //Converting Uppercase into Lowercase
        System.out.println(s2);                                     //Printing the lowercase String
    }
}
