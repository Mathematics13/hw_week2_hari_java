package java_homework;

/**Program 11
 * Java Program to display the following pattern
 * Sample pattern is given
 */

public class Program11_Pattern
{
    public static void main(String[] args)                    //Main method declaration
    {
       String a = "J    a    v     v     a";                  //Local variable declaration and initialization
       String b = "J   a a    v    v     a  a";
       String c = "J   J aaaaa           v v  aaaaa";
       String d = "J   J a   a           v a  a";

        System.out.println(a);                              //Printing all local variables
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
